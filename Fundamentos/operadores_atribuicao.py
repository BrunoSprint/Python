# Inicialização de saldo
saldo = 500
print(saldo)  # Exibe: 500

# Atribuição simples
saldo = 200
print(saldo)  # Exibe: 200

# Soma e atribuição
saldo += 10  # Adiciona 10 ao saldo
print(saldo)  # Exibe: 210

# Subtração e atribuição
saldo -= 5  # Subtrai 5 do saldo
print(saldo)  # Exibe: 205

# Divisão inteira e atribuição
saldo //= 2  # Divide o saldo por 2 (apenas a parte inteira)
print(saldo)  # Exibe: 102

# Divisão real e atribuição
saldo /= 2  # Divide o saldo por 2 (resultado com casas decimais)
print(saldo)  # Exibe: 51.0

# Multiplicação e atribuição
saldo *= 10  # Multiplica o saldo por 10
print(saldo)  # Exibe: 510.0

# Módulo e atribuição
saldo %= 4  # Calcula o resto da divisão do saldo por 4
print(saldo)  # Exibe: 2.0

# Exponenciação e atribuição
saldo **= 2  # Eleva o saldo ao quadrado
print(saldo)  # Exibe: 4.0
