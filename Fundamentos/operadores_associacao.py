# Lista de frutas
frutas = ["limao", "uva"]

# String do nome do curso
curso = "Curso de python"

# Verifica se "laranja" NÃO está na lista frutas
print("laranja" not in frutas)  # Retorna True, pois "laranja" não está na lista

# Verifica se "limao" está na lista frutas
print("limao" in frutas)  # Retorna True, pois "limao" está na lista

# Verifica se "Python" (com P maiúsculo) está na string curso
print("Python" in curso)  # Retorna False, pois a busca é sensível a maiúsculas
