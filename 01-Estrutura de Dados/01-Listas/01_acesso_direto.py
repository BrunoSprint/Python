# Lista de frutas
frutas = ["maçã", "laranja", "uva", "pera"]

# Acessando o elemento no índice 0 (primeiro elemento)
print(frutas[0])  # Saída: maçã

# Acessando o elemento no índice 2 (terceiro elemento)
print(frutas[2])  # Saída: uva


"""Conclusão
Acesso a Elementos em Listas:

Em Python, os elementos de uma lista podem ser acessados diretamente utilizando seus índices.
O índice é uma forma de numerar os elementos, começando do 0.
Flexibilidade com Índices:

É possível acessar elementos individuais de uma lista com precisão.
Além disso, você pode modificar os elementos acessados ou utilizá-los em cálculos e operações.
Essa abordagem é útil em cenários que exigem a manipulação de dados específicos em coleções ordenadas, como listas. """