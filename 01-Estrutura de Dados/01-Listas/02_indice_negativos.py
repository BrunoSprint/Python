# Lista de frutas
frutas = ["maçã", "laranja", "uva", "pera"]

# Acessando o último elemento da lista
print(frutas[-1])  # Saída: pera

# Acessando o terceiro elemento da lista, de trás para frente
print(frutas[-3])  # Saída: laranja


"""Índices Negativos em Python:

Permitem acessar elementos a partir do final da lista, simplificando operações em estruturas ordenadas.
São úteis em situações em que o tamanho exato da lista pode variar, mas o foco é no último ou penúltimo elemento.
Flexibilidade:

Python oferece tanto índices positivos quanto negativos, permitindo trabalhar com listas de forma mais intuitiva e eficiente."""