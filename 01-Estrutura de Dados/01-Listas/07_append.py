# Inicializa uma lista vazia
lista = []

# Adiciona elementos de diferentes tipos
lista.append(1)  # Adiciona o número inteiro 1
lista.append("Python")  # Adiciona a string "Python"
lista.append([40, 30, 20])  # Adiciona uma lista como elemento

# Exibe a lista final
print(lista)  # Saída: [1, "Python", [40, 30, 20]]


"""Vantagens do Método append
Facilidade de Uso:

Adicionar elementos a uma lista existente é simples e direto.
Flexibilidade:

Permite adicionar qualquer tipo de dado, incluindo estruturas compostas, como listas aninhadas.
Eficiência:

Adiciona o elemento diretamente no final da lista, sem necessidade de recriar a lista inteira.
Conclusão
Método append:

É uma ferramenta fundamental para a construção de listas dinâmicas em Python.
Ideal para adicionar elementos a listas de forma incremental.
Aplicação Prática:

Usado em situações onde os elementos de uma lista são adicionados ao longo do tempo, como ao processar dados em tempo real ou montar coleções dinamicamente."""