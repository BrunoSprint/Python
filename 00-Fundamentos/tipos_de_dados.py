# Soma de inteiros
print(11 + 10 + 1000)  # Saída: 1021

# Soma de números de ponto flutuante (float)
print(1.5 + 1 + 0.5)  # Saída: 3.0

# Valor booleano True
print(True)  # Saída: True

# Valor booleano False
print(False)  # Saída: False

# Impressão de uma string
print("Python")  # Saída: Python
