# Inicializa as variáveis
saldo = 200  # Saldo da conta
saque = 200  # Valor a ser sacado

# Comparação de igualdade
print(saldo == saque)  # Retorna True, pois saldo e saque são iguais

# Comparação de diferença
print(saldo != saque)  # Retorna False, pois saldo e saque não são diferentes

# Comparação de maior que
print(saldo > saque)  # Retorna False, pois saldo não é maior que saque

# Comparação de maior ou igual
print(saldo >= saque)  # Retorna True, pois saldo é igual a saque

# Comparação de menor que
print(saldo < saque)  # Retorna False, pois saldo não é menor que saque

# Comparação de menor ou igual
print(saldo <= saque)  # Retorna True, pois saldo é igual a saque
